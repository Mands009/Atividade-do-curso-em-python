#Programa que peça dois números inteiros e um real e calcule

n1 = int(input("Digite um numero inteiro: "))
n2 = int(input("Digite um numero inteiro: "))
n3 = float(input("Digite um numero real: "))

oper1 = (n1*2)*(n2/2)
oper2 = (n1*3)+n3
oper3 = n3**3

print("Operação 1: ", oper1)
print("Operação 2: ", oper2)
print("Operação 3: ", oper3)